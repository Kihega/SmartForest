    "                    </td>\n"
    "                    <td style={S.td}>\n"
    "                      <span style={s.is_alert\n"
    "                        ? {...S.badge, background:'#fee2e2', color:'#dc2626'}\n"
    "                        : {...S.badge, background:'#dcfce7', color:'#16a34a'}}>\n"
    "                        {s.is_alert ? '🔴 Alert' : '🟢 Normal'}\n"
    "                      </span>\n"
    "                    </td>\n"
    "                    <td style={{...S.td, color:'#9ca3af', fontSize:12}}>\n"
